    private void BtnLoginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BtnLoginActionPerformed
        // TODO add your handling code here:
        try {
            Connection con = Connectionget.getInstatnce().getConnection();
            PreparedStatement pstm = con.prepareStatement("select * from useracc where username ='" + UsernameLogin.getText() + "'");
            ResultSet rs = pstm.executeQuery();

            if ("".equals(UsernameLogin.getText())) {
                JOptionPane.showMessageDialog(null, "Please Enter Username & Password");
            } else if (rs.next()) {

                String password = rs.getString("password");
                String job = rs.getString("jobTitle");
                String Name = rs.getString("name");

                if (Passwordlogin.getText().equals(password)) {
                    switch (job) {
                        case "cashier":
                            Dashboard cashi = new Dashboard();
                            cashi.setVisible(true);
                            cashi.jLabel9.setText(Name);
                            setVisible(false);
                            break;
                        case "manager":
                            AdminDashboard mng = new AdminDashboard();

                            mng.setVisible(true);
                            mng.jLabel1.setText("Manager Dashboard");
                            mng.jButton7.setEnabled(false);
                            mng.jLabel18.setEnabled(false);
                            mng.jLabel6.setText(Name);

                            setVisible(false);
                            //System.out.println("Maneger");
                            break;
                        default:
                            //System.out.println("Admin");
                            AdminDashboard admin = new AdminDashboard();
                            admin.setVisible(true);
                            admin.jLabel1.setText("Admin Dashboard");
                            admin.jLabel6.setText(Name);
                            setVisible(false);
                            break;
                    }
                } else {
                    JOptionPane.showMessageDialog(null, "Invalid Password");
                }

            } else {
                JOptionPane.showMessageDialog(null, "Invalid UserName");

            }
        } catch (HeadlessException | SQLException e) {
            JOptionPane.showMessageDialog(null, e);
        } catch (ClassNotFoundException ex) {
            Logger.getLogger(UpdateCustomer.class.getName()).log(Level.SEVERE, null, ex);
        }


    }//GEN-LAST:event_BtnLoginActionPerformed

